a = "3.1415926"

# 把 a 转换为 浮点数int
x = float(a)
print("{}转换为 浮点数：{}".format(a, x))  # 3.1415926转换为 浮点数：3.1415926

# 把 a 转换为整数
y = int(float(a))
print("{}转换为 浮点数：{}".format(a, y))  # 3.1415926转换为 浮点数：3

# 把 a 转换为整数，再转换为浮点数
z = float(int(float(a)))
print("{}转换为 浮点数：{}".format(a, z))  # 3.1415926转换为 浮点数：3.0

# 把 a 转换为仅有两个小数位的字符串
i = ("%.2f" % float(a))
print("{}转换为 浮点数：{}".format(a, i))  # 3.1415926转换为 浮点数：3.14
